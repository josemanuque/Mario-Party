    private void initButtons(){
        for (int i = 0; i < opcionArray.length; i++){
            opcionArray[i] = new JButton();
            opcionArray[i].setActionCommand(i + "");
            opcionArray[i].setBackground(chooseColor(i));
            jPanel1.add(opcionArray[i]);
            opcionArray[i].setBounds(0, i * 40, 200, 40);
            opcionArray[i].addActionListener(new ActionListener() {
                public void actionPerformed(java.awt.event.ActionEvent evt) {
                    
                    buttonPressed(evt);
                }
                });
        }
    }
    
    public void buttonPressed(java.awt.event.ActionEvent evt){
        JButton botonTemp = (JButton)evt.getSource();
        
        
        if(numJugador == currentPlayer) {
            color = botonTemp.getBackground();
            //currentPlayer++;
            botonTemp.setEnabled(false);
            try {
                this.mandarColor(color);
            } catch (IOException ex) {
                Logger.getLogger(StartFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }
    
    
    public Color chooseColor(int color){
        switch(color){
            case 0 ->{
                return new Color(220,20,60); // red
            }
            case 1 ->{
                return new Color(138,43,226); // purple
            }
            case 2 ->{
                return Color.BLUE;
            }
            case 3 ->{
                return Color.GREEN;
            }
            case 4 ->{
                return Color.MAGENTA;
            }
            case 5 ->{
                return Color.ORANGE;
            }
            case 6 ->{
                return Color.PINK;
            }
            case 7 ->{
                return Color.CYAN;
            }
            case 8 ->{
                return Color.DARK_GRAY;
            }
            case 9 ->{
                return new Color(85, 107, 47); // weird green
            }
            
        }
        return null;
    }
    
    

