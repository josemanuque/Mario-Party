    public void recibirColor(String color) { //Método que recibe un color y actualiza la lista de colores elegidos. 
        coloresElegidos.addLast(color);
        currentPlayer++;
        etiquetaElegir.setText("Jugador " + currentPlayer + " elija su color de ficha:");
        System.out.println(nick + " recibió " + color);
        for(int i = 0; i < 10; i++) {
            if(color.equals(opcionArray[i].getBackground().toString())) {
                System.out.println("Color en posición: " + i); 
                opcionArray[i].setEnabled(false); }
        }
       }
       
    
    public void mandarColor(Color color) throws IOException {
        etiquetaElegir.setText("Jugador " + currentPlayer + " elija su color de ficha:");
        coloresElegidos.add(color.toString());
        cliente.salida.writeInt(1);
        cliente.salida.writeUTF(color.toString());
        if(lastPlayer) {
            cliente.salida.writeInt(2);
        }
    }

